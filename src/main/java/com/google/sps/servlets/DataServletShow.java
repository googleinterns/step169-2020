// Servlet which sends out the client's login status
@WebServlet("/show")
public class DataServletShow extends HttpServlet {
  
  //   Determines if the content should be shown or not if user is logged in
  @Override
  public void doGet(HttpServletRequest request, HttpServletResponse response) throws IOException {
    response.setContentType("text/html");
    PrintWriter out = response.getWriter();

    // Determine if the page should show content or not based on user login status
    UserService userService = UserServiceFactory.getUserService();
    if (userService.isUserLoggedIn()) {
      out.print("yes");
    } else {
      out.print("no");
    }
  }
}
